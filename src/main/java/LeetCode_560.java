public class LeetCode_560 {
    /**
     * LeetCode 560 : 和为 K 的子数组
     *
     * <pre>
     * 题意：
     * 给你一个整数数组 nums 和一个整数 k，
     * 统计数组中“连续子数组的和等于 k”的个数。
     *
     * -----------------------------------------
     * 核心思路：前缀和 + 哈希表
     * -----------------------------------------
     * 1. 定义前缀和 sum：
     *    sum 表示从 nums[0] 一直加到当前下标 i 的累加和。
     *
     * 2. 假设当前来到位置 i，
     *    当前前缀和为 sum。
     *
     * 3. 如果之前某个位置的前缀和是 pre，
     *    并且满足：
     *        sum - pre = k
     *    那么：
     *        pre = sum - k
     *
     * 4. 这说明：
     *    只要之前出现过前缀和 "sum - k"，
     *    那么从那个前缀和之后的位置到当前 i 的这一段连续子数组，
     *    它们的累加和就一定是 k。
     *
     * 5. 所以：
     *    每来到一个新位置，只需要去哈希表里查：
     *        map 中有没有出现过 (sum - k)
     *    如果有，出现了几次，就说明有几个不同的子数组
     *    以当前位置结尾，且累加和等于 k。
     *
     * 6. 哈希表 map 的含义：
     *    key   = 某个前缀和
     *    value = 这个前缀和出现过多少次
     *
     * 7. 为什么一开始要放：
     *        map.put(0, 1)
     *    因为这表示：
     *    “在一个数都还没遍历之前，前缀和 0 已经出现过 1 次”
     *    这样当某一段前缀和本身就等于 k 时，也能正确统计到。
     *
     * -----------------------------------------
     * 公式本质
     * -----------------------------------------
     * 设：
     *    prefix[i] = nums[0] + nums[1] + ... + nums[i]
     *
     * 若子数组 nums[j+1..i] 的和为 k，
     * 则有：
     *    prefix[i] - prefix[j] = k
     *
     * 移项得：
     *    prefix[j] = prefix[i] - k
     *
     * 所以问题就变成：
     *    当我知道当前位置的前缀和 prefix[i] 时，
     *    去找之前有没有出现过 prefix[i] - k。
     *
     * -----------------------------------------
     * 例子详细拆解
     * -----------------------------------------
     * 例子：
     *    nums = [1, -1, 2, 3, -2, 4]
     *    k = 3
     *
     * 初始化：
     *    sum = 0
     *    ans = 0
     *    map = {0=1}
     *
     * 这里的 map = {0=1} 表示：
     *    还没开始遍历数组时，前缀和 0 已经出现过 1 次。
     *
     * -----------------------------------------
     * 第 1 步：来到 nums[0] = 1
     * -----------------------------------------
     * 当前：
     *    sum = 0 + 1 = 1
     *
     * 需要查找：
     *    sum - k = 1 - 3 = -2
     *
     * map 中没有 -2，
     * 说明：没有任何以前的前缀和能和当前 sum 配出一个和为 k 的子数组。
     *
     * 所以：
     *    ans 不变，仍然是 0
     *
     * 然后把当前前缀和 1 记入 map：
     *    map = {0=1, 1=1}
     *
     * -----------------------------------------
     * 第 2 步：来到 nums[1] = -1
     * -----------------------------------------
     * 当前：
     *    sum = 1 + (-1) = 0
     *
     * 需要查找：
     *    sum - k = 0 - 3 = -3
     *
     * map 中没有 -3，
     * 说明当前也没有以位置 1 结尾、累加和为 3 的子数组。
     *
     * 所以：
     *    ans 仍然是 0
     *
     * 然后更新前缀和 0 的出现次数：
     *    map = {0=2, 1=1}
     *
     * 注意：
     *    前缀和 0 出现了两次，
     *    一次是“还没开始遍历之前”，
     *    一次是“遍历到下标 1 时”。
     *
     * -----------------------------------------
     * 第 3 步：来到 nums[2] = 2
     * -----------------------------------------
     * 当前：
     *    sum = 0 + 2 = 2
     *
     * 需要查找：
     *    sum - k = 2 - 3 = -1
     *
     * map 中没有 -1，
     * 所以：
     *    ans 仍然是 0
     *
     * 然后记录当前前缀和：
     *    map = {0=2, 1=1, 2=1}
     *
     * -----------------------------------------
     * 第 4 步：来到 nums[3] = 3
     * -----------------------------------------
     * 当前：
     *    sum = 2 + 3 = 5
     *
     * 需要查找：
     *    sum - k = 5 - 3 = 2
     *
     * map 中有 2，并且出现了 1 次，
     * 说明：
     *    有 1 个以前的前缀和，能够和当前 sum 组成差值 3
     *
     * 所以：
     *    ans = ans + 1 = 1
     *
     * 这个子数组是谁？
     *
     * 以前出现前缀和 2 的位置，是下标 2 处。
     * 也就是说：
     *    nums[3..3] = [3]
     * 的累加和就是 3。
     *
     * 然后记录当前前缀和：
     *    map = {0=2, 1=1, 2=1, 5=1}
     *
     * -----------------------------------------
     * 第 5 步：来到 nums[4] = -2
     * -----------------------------------------
     * 当前：
     *    sum = 5 + (-2) = 3
     *
     * 需要查找：
     *    sum - k = 3 - 3 = 0
     *
     * map 中有 0，并且出现了 2 次，
     * 这说明：
     *    有 2 个以前的位置，它们的前缀和都是 0，
     *    所以会产生 2 个不同的、以当前位置结尾的合法子数组。
     *
     * 所以：
     *    ans = 1 + 2 = 3
     *
     * 这 2 个子数组分别是：
     *
     *    ① 从开头到当前位置：
     *       nums[0..4] = [1, -1, 2, 3, -2]
     *       累加和 = 3
     *
     *    ② 从前缀和第二次为 0 之后开始，到当前位置：
     *       nums[2..4] = [2, 3, -2]
     *       累加和 = 3
     *
     * 然后记录当前前缀和：
     *    map = {0=2, 1=1, 2=1, 3=1, 5=1}
     *
     * -----------------------------------------
     * 第 6 步：来到 nums[5] = 4
     * -----------------------------------------
     * 当前：
     *    sum = 3 + 4 = 7
     *
     * 需要查找：
     *    sum - k = 7 - 3 = 4
     *
     * map 中没有 4，
     * 所以：
     *    ans 不变，仍然是 3
     *
     * 然后记录当前前缀和：
     *    map = {0=2, 1=1, 2=1, 3=1, 5=1, 7=1}
     *
     * -----------------------------------------
     * 遍历结束
     * -----------------------------------------
     * 最终答案：
     *    ans = 3
     *
     * 也就是说，一共有 3 个连续子数组的和等于 3。
     *
     * 它们分别是：
     *    [3]
     *    [1, -1, 2, 3, -2]
     *    [2, 3, -2]
     *
     * -----------------------------------------
     * 代码执行流程总结
     * -----------------------------------------
     * 每遍历到一个数 num，都执行下面 4 件事：
     *
     * 1. 更新当前前缀和
     *        sum += num
     *
     * 2. 去 map 中查找 sum - k 出现过几次
     *        如果出现过 count 次，
     *        说明有 count 个子数组以当前位置结尾，且和为 k
     *
     * 3. 把这个次数累加到答案里
     *        ans += map.getOrDefault(sum - k, 0)
     *
     * 4. 把当前前缀和 sum 放入 map
     *        map.put(sum, map.getOrDefault(sum, 0) + 1)
     *
     * -----------------------------------------
     * 时间复杂度
     * -----------------------------------------
     * O(N)
     * 每个元素只遍历一次，哈希表查询和更新平均都是 O(1)。
     *
     * -----------------------------------------
     * 空间复杂度
     * -----------------------------------------
     * O(N)
     * 最坏情况下，每个前缀和都不同，都要存进哈希表。
     * </pre>
     */
    public int subarraySum(int[] nums, int k) {
        // key = 前缀和
        // value = 这个前缀和出现的次数
        Map<Integer, Integer> map = new HashMap<>();
        // 前缀和数组
        int[] prefix = new int[nums.length];
        for (int i = 1; i <= nums.length; i++) {
            prefix[i] = prefix[i - 1] + nums[i - 1];
        }
        // 最后结果
        int result = 0;
        // 前缀和 0 先出现一次
        map.put(0, 1);
        // 遍历前缀和数组
        for (int i = 0; i <= nums.length; i++) {
            // 当前需要找的历史前缀和
            int need = prefix[i] - k;
            // 这个历史前缀和出现过多少次
            // 有 count 个子数组，以当前位置结尾，和等于 k
            int count = map.getOrDefault(need, 0);
            // 出现了多少次
            result = result + count;
            // 把当前前缀和 prefix[i] 记到 map 里，留给后面的元素使用
            int cur = prefix[i];
            // 当前前缀和以前出现过几次
            Integer oldTimes = map.getOrDefault(cur, 0);
            // 把当前这一次也记录进去
            map.put(cur, oldTimes + 1);

        }

        return result;
    }
}
